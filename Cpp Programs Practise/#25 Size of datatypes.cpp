// type casting
#include <iostream>
using namespace std;

int main() {
	bool p = true;
	char x = 'X';
	short y = 132;
	int a = 20;
	float b = 10.56;
	long l = 1223.23;
	double d = 1212323.3434;
	
	cout << sizeof(p) << endl;
	cout << sizeof(x) << endl;
	cout << sizeof(y) << endl;
	cout << sizeof(a) << endl;
	cout << sizeof(b) << endl;
	cout << sizeof(l) << endl;
	cout << sizeof(d) << endl;
	return 0;
}
